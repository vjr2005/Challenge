import Foundation

/// Design tokens for opacity values.
public enum OpacityToken {
	/// Subtle opacity (0.1)
	public static let subtle: Double = 0.1

	/// Light opacity (0.15)
	public static let light: Double = 0.15

	/// Medium opacity (0.4)
	public static let medium: Double = 0.4

	/// Heavy opacity (0.6)
	public static let heavy: Double = 0.6

	/// Almost opaque (0.8)
	public static let almostOpaque: Double = 0.8
}
