import Foundation

/// Design tokens for border width values.
public enum BorderWidthToken {
	/// Hairline border (0.5pt)
	public static let hairline: CGFloat = 0.5

	/// Thin border (1pt)
	public static let thin: CGFloat = 1

	/// Medium border (2pt)
	public static let medium: CGFloat = 2

	/// Thick border (4pt)
	public static let thick: CGFloat = 4
}
